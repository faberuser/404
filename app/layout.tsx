import type { Metadata } from "next"
import { Geist, Geist_Mono } from "next/font/google"
import "./globals.css"
import { ThemeProvider } from "@/app/theme-provider"

const metadataTitle = process.env.NEXT_PUBLIC_METADATA_TITLE
    ? process.env.NEXT_PUBLIC_METADATA_TITLE
    : "404 Not Found"

const metadataDescription = process.env.NEXT_PUBLIC_METADATA_DESCRIPTION
    ? process.env.NEXT_PUBLIC_METADATA_DESCRIPTION
    : "The page you are looking for does not exist."

const geistSans = Geist({
    variable: "--font-geist-sans",
    subsets: ["latin"],
})

const geistMono = Geist_Mono({
    variable: "--font-geist-mono",
    subsets: ["latin"],
})

export const metadata: Metadata = {
    title: metadataTitle,
    description: metadataDescription,
}

export default function RootLayout({
    children,
}: Readonly<{
    children: React.ReactNode
}>) {
    return (
        <html lang="en" suppressHydrationWarning>
            <body
                className={`${geistSans.variable} ${geistMono.variable} antialiased`}
            >
                <ThemeProvider
                    attribute="class"
                    defaultTheme="system"
                    enableSystem
                    disableTransitionOnChange
                >
                    {children}
                </ThemeProvider>
            </body>
        </html>
    )
}
